package week05;

public class ForLoopExamples {
    public static void main(String[] args) {
        // Print "Hello!" 10 times
        for (int i = 1; i <= 10; i++) {
            System.out.println("Hello!");
        }
        //Print hi and number together

        for (int i = 1; i <= 5; i++) {
            System.out.println("Hi - " + i);
        }

        // print Bye 6 to 1.
        for (int i = 6; i >= 1; i--) {
            System.out.println("Bye - " + i);
        }


    }
}
